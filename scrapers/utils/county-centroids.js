/**
 * Get the approximate centroid for a US county
 * @param {string} county - County name (e.g., "Montgomery", "Prince Georges")
 * @param {string} stateCode - Two-letter state code (e.g., "MD", "VA")
 * @returns {{ lat: number, lng: number } | null}
 */
function getCountyCentroid(county, stateCode) {
  if (!county || !stateCode) return null;

  const state = stateCode.toUpperCase().trim();
  const stateData = COUNTY_DATA[state];
  if (!stateData) return null;

  // Try exact match first
  const countyTrimmed = county.trim();
  if (stateData[countyTrimmed]) {
    return stateData[countyTrimmed];
  }

  // Try case-insensitive match
  const countyLower = countyTrimmed.toLowerCase();
  for (const [name, coords] of Object.entries(stateData)) {
    if (name.toLowerCase() === countyLower) {
      return coords;
    }
  }

  // Try partial match (e.g., "Prince George's" vs "Prince Georges")
  const countyNormalized = countyLower.replace(/['']/g, '').replace(/\s+county$/i, '');
  for (const [name, coords] of Object.entries(stateData)) {
    const nameNormalized = name.toLowerCase().replace(/['']/g, '').replace(/\s+county$/i, '');
    if (nameNormalized === countyNormalized) {
      return coords;
    }
  }

  return null;
}

module.exports = { getCountyCentroid, COUNTY_DATA };
